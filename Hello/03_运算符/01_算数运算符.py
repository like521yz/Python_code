#算数运算符：
# 算术运算符用于执行基本的数学运算，常见类型及说明：
# + : 加法 - 两数相加
# - : 减法 - 两数相减
# * : 乘法 - 两数相乘
# / : 除法 - 两数相除，结果为浮点数
# //: 整除 - 两数相除，结果为整数（向下取整）
# % : 取余 - 返回除法运算后的余数
# **: 幂运算 - 计算一个数的几次幂
a = 3
b = 10

print(f"整除{a}//{b} = {a//b}")
print(f"除法{b}/{a} = {b/a}")
print(f"取余{b}%{a} = {b%a}")
print(f"取余{a}%{b} = {a%b}")
print(f"幂运算{a}**{b} = {a**b}")