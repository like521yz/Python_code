

#集合：由不重复元素组成的无序容器
#集合不支持索引
#床创建空集合只能用set() 不能用{}，因为{}创建的是空字典

set_empty = set()
print(f"set_empty={set_empty}；类型是：{type(set_empty)}")

set_a = {1,2,3,4,5}
print(f"set_a={set_a}；类型是：{type(set_a)}")
print(f"set_a={set_a}；类型是：{type(set_a)}")

set_b = {"apple","banana","orange","apple"}
print(f"set_b={set_b}；类型是：{type(set_b)}")
print(f"set_b={set_b}；类型是：{type(set_b)}")


